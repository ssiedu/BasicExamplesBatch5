
public class IfElseDemo {
    public static void main(String[] args) {
        int day=12;
        
        if(day==1){
            System.out.println("Monday");
        }else if(day==2){
            System.out.println("Tuesday");
        }else if(day==3){
            System.out.println("Wedensday");
        }else{
            System.out.println("Wrong Input");
        }
        
        
        
    }
}
